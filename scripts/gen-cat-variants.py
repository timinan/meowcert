# Tone-mapping helper — for a base RGB, return its HSL.
def rgb_to_hls(r, g, b):
    return colorsys.rgb_to_hls(r / 255.0, g / 255.0, b / 255.0)


def hls_to_rgb(h, l, s):
    r, g, b = colorsys.hls_to_rgb(h, l, s)
    return (int(round(r * 255)), int(round(g * 255)), int(round(b * 255)))


def shift_palette(palette, target_h_deg, target_s, light_bias=0.0):
    """Map each base shade to the new hue, preserving original lightness ordering.
    Saturation set to target_s. light_bias shifts lightness ± (clamped to [0,1])."""
    out = []
    for (r, g, b) in palette:
        h, l, s = rgb_to_hls(r, g, b)
        nl = max(0.0, min(1.0, l + light_bias))
        out.append(hls_to_rgb(target_h_deg / 360.0, nl, target_s))
    return out


def shift_palette_naturalistic(palette, target_h_deg, target_s_base):
    """For subtle/natural cat colors — keep low saturation, just shift hue.
    target_s_base is a multiplier (0..1) on natural fur saturation."""
    out = []
    for (r, g, b) in palette:
        h, l, s = rgb_to_hls(r, g, b)
        # Naturalistic: cap saturation, keep desaturated overall
        new_s = min(target_s_base, s + 0.25)
        out.append(hls_to_rgb(target_h_deg / 360.0, l, new_s))
    return out


