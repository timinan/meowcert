"""Generate new cat variants from a base cat shape.

Strategy: cat13 (Butters) is a clean grey shorthair with distinct ear-pink + the
new orange-iris eye fix. Its body fur is a clean 5-shade grey palette. We map
those 5 grey shades to new hues to generate single-tone cats, head/body split
cats, and naturalistic-tabby cats — leaving ears, eyes, and outlines untouched.

Outputs to:
  assets-raw/cat<N>/cat<N>_<anim>_<NN>.png  (one new cat dir per variant)
  tools/cats/cats.json  (appends stub entries with autogenerated names)

Usage:
  python3 scripts/gen-cat-variants.py        # generates the full curated set
  python3 scripts/gen-cat-variants.py --dry  # report what would be made
"""
